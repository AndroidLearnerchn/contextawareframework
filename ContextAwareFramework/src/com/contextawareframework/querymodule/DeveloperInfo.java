package com.contextawareframework.querymodule;

import android.content.Context;

public class DeveloperInfo{
	
	private static String developerEmailId;
	private static String AppPackageName;
	private static String APIKey;
	
	/*
	 * 
	 */
	public static String getDeveloperEmailId() {
		return developerEmailId;
	}
	/*
	 * 
	 */
	public static String setDeveloperEmailId(String devEmailId) {
		developerEmailId = devEmailId;
		return developerEmailId;
	}
	/*
	 * 
	 */
	public static String getAppPackageName()
	{
		return AppPackageName;
	}
	/*
	 * 
	 */
	public static String setAppPackageName(Context context) {
		AppPackageName = context.getApplicationContext().getPackageName();
		return AppPackageName;
	}
	/*
	 * 
	 */
	public static String getAPIKey() {
		return APIKey;
	}
	/*
	 * 
	 */
	public static String setAPIKey(String aPIKey) {
		APIKey = aPIKey;
		return APIKey;
	}
		
}
